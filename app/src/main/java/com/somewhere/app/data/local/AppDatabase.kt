package com.somewhere.app.data.local

import android.content.Context
import androidx.room.Database
import androidx.room.RoomDatabase
import androidx.room.Room
import androidx.room.migration.Migration
import androidx.sqlite.db.SupportSQLiteDatabase
import com.somewhere.app.data.model.Drop

@Database(entities = [Drop::class], version = 8, exportSchema = false)
abstract class AppDatabase : RoomDatabase() {

    abstract fun dropDao(): DropDao

    companion object {
        val MIGRATION_1_2 = object : Migration(1, 2) {
            override fun migrate(database: SupportSQLiteDatabase) {
                database.execSQL("ALTER TABLE drops ADD COLUMN audioPath TEXT")
            }
        }

        val MIGRATION_2_3 = object : Migration(2, 3) {
            override fun migrate(database: SupportSQLiteDatabase) {
                database.execSQL("ALTER TABLE drops ADD COLUMN authorName TEXT")
            }
        }

        val MIGRATION_3_4 = object : Migration(3, 4) {
            override fun migrate(database: SupportSQLiteDatabase) {
                database.execSQL("ALTER TABLE drops ADD COLUMN authorAvatarUrl TEXT")
            }
        }

        val MIGRATION_4_5 = object : Migration(4, 5) {
            override fun migrate(database: SupportSQLiteDatabase) {
                database.execSQL("ALTER TABLE drops ADD COLUMN authorId TEXT")
            }
        }

        val MIGRATION_5_6 = object : Migration(5, 6) {
            override fun migrate(database: SupportSQLiteDatabase) {
                database.execSQL("ALTER TABLE drops ADD COLUMN expiresAt INTEGER")
                database.execSQL("ALTER TABLE drops ADD COLUMN isAnonymous INTEGER NOT NULL DEFAULT 0")
                database.execSQL("ALTER TABLE drops ADD COLUMN category TEXT")
            }
        }

        val MIGRATION_6_7 = object : Migration(6, 7) {
            override fun migrate(database: SupportSQLiteDatabase) {
                database.execSQL("ALTER TABLE drops ADD COLUMN isDeadDrop INTEGER NOT NULL DEFAULT 0")
                database.execSQL("ALTER TABLE drops ADD COLUMN fileUrl TEXT")
                database.execSQL("ALTER TABLE drops ADD COLUMN fileType TEXT")
                database.execSQL("ALTER TABLE drops ADD COLUMN fileName TEXT")
                database.execSQL("ALTER TABLE drops ADD COLUMN fileSize INTEGER")
            }
        }

        val MIGRATION_7_8 = object : Migration(7, 8) {
            override fun migrate(database: SupportSQLiteDatabase) {
                database.execSQL("ALTER TABLE drops ADD COLUMN isHitchhiker INTEGER NOT NULL DEFAULT 0")
                database.execSQL("ALTER TABLE drops ADD COLUMN carriedByUserId TEXT")
            }
        }

        @Volatile
        private var INSTANCE: AppDatabase? = null

        fun getInstance(context: Context): AppDatabase {
            return INSTANCE ?: synchronized(this) {
                val instance = Room.databaseBuilder(
                    context.applicationContext,
                    AppDatabase::class.java,
                    "somewhere_db"
                )
                    .addMigrations(MIGRATION_1_2, MIGRATION_2_3, MIGRATION_3_4, MIGRATION_4_5, MIGRATION_5_6, MIGRATION_6_7, MIGRATION_7_8)
                    .build()
                INSTANCE = instance
                instance
            }
        }
    }
}
